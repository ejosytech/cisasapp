package com.ejosy.cisasapp;

public class ObjectClient {
     public int id;
    public String  timestamp;
    public String name;
    public String phone;
    public String avenue;
    public String street;


    public ObjectClient(int id, String timestamp, String name, String phone,String avenue, String street){

        this.id = id;
        this.timestamp = timestamp;
        this.name = name;
        this.phone = phone;
        this.avenue = avenue;
        this.street = street;
    }

}
